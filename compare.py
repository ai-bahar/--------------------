def مقایسه(اطلاعات, ویژگی):
    """
    ورودی:
      اطلاعات → لیست شامل تاپل‌های (نام لپ‌تاپ، مقدار ویژگی)
      ویژگی → نام ستون دیتابیس (مثل 'رم' یا 'قیمت')

    خروجی:
      متن توضیحی برای کاربر
    """

    if len(اطلاعات) < 2:
        return "برای مقایسه باید حداقل دو لپ‌تاپ مشخص شود."

    لپتاپ1, مقدار1 = اطلاعات[0]
    لپتاپ2, مقدار2 = اطلاعات[1]

    # اگر یکی از مقادیر خالی بود
    if مقدار1 is None or مقدار2 is None:
        return "اطلاعات کافی برای مقایسه وجود ندارد."

    # منطق مقایسه بر اساس نوع ویژگی
    if ویژگی in ["رم", "حافظه"]:  # بیشتر بودن بهتر است
        if مقدار1 > مقدار2:
            return f"{لپتاپ1} {ویژگی} بیشتری دارد نسبت به {لپتاپ2}."
        elif مقدار2 > مقدار1:
            return f"{لپتاپ2} {ویژگی} بیشتری دارد نسبت به {لپتاپ1}."
        else:
            return f"هر دو لپ‌تاپ {ویژگی} یکسانی دارند."

    elif ویژگی == "قیمت":  # کمتر بودن بهتر است
        if مقدار1 < مقدار2:
            return f"{لپتاپ1} قیمت مناسب‌تری دارد نسبت به {لپتاپ2}."
        elif مقدار2 < مقدار1:
            return f"{لپتاپ2} قیمت مناسب‌تری دارد نسبت به {لپتاپ1}."
        else:
            return "هر دو لپ‌تاپ قیمت یکسانی دارند."

    elif ویژگی == "وزن":  # کمتر بودن بهتر است
        if مقدار1 < مقدار2:
            return f"{لپتاپ1} سبک‌تر است نسبت به {لپتاپ2}."
        elif مقدار2 < مقدار1:
            return f"{لپتاپ2} سبک‌تر است نسبت به {لپتاپ1}."
        else:
            return "هر دو لپ‌تاپ وزن یکسانی دارند."

    else:
        # ویژگی‌های متنی مثل CPU، GPU، صفحه‌نمایش، باتری
        if مقدار1 != مقدار2:
            return f"{لپتاپ1} دارای {مقدار1} است و {لپتاپ2} دارای {مقدار2}."
        else:
            return f"هر دو لپ‌تاپ {ویژگی} مشابهی دارند ({مقدار1})."
            